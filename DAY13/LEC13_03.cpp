int sol(int** a) {
  int rlt = -2e9;
  
  for (int i = 0; i < 92; ++i) {
    int prt = 0;
    for (int j = 0; j < 8; ++j) {
      int c = j;
      int r = nq[i][c]-1;    
      prt += a[r][c];
    }
    rlt = max(rlt, prt);
  }
  return rlt;
}
